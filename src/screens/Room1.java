	public Room1() {
		super();
		imgs = getImgs();
		setPlayerX(20);
		setPlayerY(300);
	}
	
	public boolean collisionDetection(int x, int y, int j, int i) {
		int x2 = j * 20;
		int y2 = i * 20;
		if(x >= x2 && x <= x2 + 20) {
			if(y >= y2 - 20 && y <= y2 + 20) {
				return true;
			}
		}
		return false;
	}
	
	public boolean imSmart() {
		int x = player.getX();
		int y = player.getY();
		for(int i = 0; i < blockField.length; i++) {
			for(int j = 0; j < blockField[0].length; j++) {
				if(blockField[i][j] == 1) {
					if(collisionDetection(x, y, j, i)) {
						player.setCollision(true);
						return true;
					}
				}
				if(blockField[i][j] == 2) {
					if(collisionDetection(x, y, j, i)) {
						changeRooms(0);
						return true;
					}
				}
			}
		}
		player.setCollision(false);
		return false;
	}
	
	@Override
	public void myDraw(Graphics window) {
		window.drawImage(imgs[0], 0, 0, 800, 640, null);
		for(int i = 0; i < blockField.length; i++) {
			for(int j = 0; j < blockField[0].length; j++) {
				if(blockField[i][j] == 1) {
					window.drawImage(imgs[5], j * 20, i * 20, 20, 20, null);
				} else if(blockField[i][j] == 2) {
					window.drawImage(imgs[5], j*20, i * 20, 20, 20, null);
				}
			}
		}
		imSmart();
		interact();
		player.draw(window);
	}
